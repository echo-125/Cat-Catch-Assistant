def traditional_to_simplified(text: str) -> str:
    """
    将繁体中文转换为简体中文

    参数:
        text: 原始文本

    返回:
        转换后的简体中文文本
    """
    if not text:
        return text

    result = []
    for char in text:
        # 查找对照表
        simplified = TRADITIONAL_TO_SIMPLIFIED.get(char, char)
        result.append(simplified)

    return ''.join(result)


def is_traditional(text: str) -> bool:
    """
    判断文本是否包含繁体中文

    参数:
        text: 文本

    返回:
        是否包含繁体中文
    """
    for char in text:
        if char in TRADITIONAL_TO_SIMPLIFIED:
            return True
    return False
